def num(num1 , num2):
    add = num1 + num2
    sub = num1 - num2
    mul = num1 * num2
    div = num1 / num2
    mod = num1 % num2 
    print ("Addition :" , add)
    print ("Subtraction :" , sub)
    print ("Multiplication :" , mul)
    print ("Division :" , div)
    print ("Modulus :" , mod)


input1 = int(input("Enter Your Number1 : "))
input2 = int(input("Enter Your Number2 : "))
num(input1, input2)

